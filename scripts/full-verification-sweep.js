// Helper to query email log
const getLatestEmail = (emailAddress, subjectKeyword) => {
  const filePath = path.join(__dirname, '../temp_test_emails.json');
  if (!fs.existsSync(filePath)) return null;
  try {
    const emails = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    const matches = emails.filter(e => 
      e.to.toLowerCase() === emailAddress.toLowerCase() && 
      e.subject.toLowerCase().includes(subjectKeyword.toLowerCase())
    );
    if (!matches.length) return null;
    return matches[matches.length - 1];
  } catch (err) {
    return null;
  }
};

const waitForEmail = async (emailAddress, subjectKeyword, timeoutMs = 15000) => {
  const start = Date.now();
  while (Date.now() - start < timeoutMs) {
    const email = getLatestEmail(emailAddress, subjectKeyword);
    if (email) return email;
    await new Promise(resolve => setTimeout(resolve, 500));
  }
  throw new Error(`Timeout waiting for email to ${emailAddress} with subject ${subjectKeyword}`);
};

